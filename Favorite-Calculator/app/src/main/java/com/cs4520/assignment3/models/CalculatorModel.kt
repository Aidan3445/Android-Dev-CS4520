package com.cs4520.assignment3.models

class CalculatorModel {
    fun add(
        a: Double,
        b: Double,
    ): Double {
        return a + b
    }

    fun subtract(
        a: Double,
        b: Double,
    ): Double {
        return a - b
    }

    fun multiply(
        a: Double,
        b: Double,
    ): Double {
        return a * b
    }

    fun divide(
        a: Double,
        b: Double,
    ): Double {
        return a / b
    }
}
